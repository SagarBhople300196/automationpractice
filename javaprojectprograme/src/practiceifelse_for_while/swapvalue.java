package practiceifelse_for_while;

public class swapvalue {
	public static void main(String[] args ) {
		int num1=60, num2=40, temp;
		System.out.println("num1 : "+num1);
		System.out.println("num2 : "+num2);
		temp=num1;
		num1=num2;
		num2=temp;
		System.out.println("num1 : "+num1);
		System.out.println("num2 : "+num2);
		
	}

}
